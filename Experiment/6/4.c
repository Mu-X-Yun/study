//这是循环结构程序设计二实验题的第四题。
//编程，百马百担问题：有100匹马，驮100担货，大马驮3担，中马驮2担，2匹小马驮一担，要求一次性驮完且没有空闲的马，大、中、小马各至少一匹，求大、中、小各多少匹。
#include <stdio.h>
int main()
{
	int x = 1, y = 2;
	for (x = 1; x < 33; x++)
	{
		for (y = 2; y < 99 - x; y += 2)
		{
			if(3*x+2*(100-x-y) + y / 2 == 100)
				printf("大马%d匹，中马%d匹，小马%d匹\n", x, 100-x-y, y);
		}
	}
	return 0;
}